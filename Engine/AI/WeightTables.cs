        public static int GetWeight(int pieceType, int location, int colorToMove) {
            if (colorToMove == Piece.Black) {
                int file = BoardUtils.FileIndex(location);
                int rank = BoardUtils.RankIndex(location);
                rank = 7 - rank;
                location = rank * 8 + file;
            }

            if (location > 64 || location < 0) { // Validate location
                throw new BoardException("Attempt to get invalid board location");
            }

            switch (pieceType) {
                case Piece.Pawn: // handle position offsets later
                    return pawnOpening[location];
                case Piece.King:
                    return 1; // handle this later too.
                case Piece.Knight:
                    return knight[location];
                case Piece.Bishop:
                    return bishop[location];
                case Piece.Rook:
                    return rook[location];
                default:
                    return 1;
            }
        }

